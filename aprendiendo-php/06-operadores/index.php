<?php

$numero1 = 50;
$numero2 = 23;

echo 'suma '.$numero1.' + '.$numero2.' = '.($numero1+$numero2).'<br>';
echo 'resta '.$numero1.' + '.$numero2.' = '.($numero1-$numero2).'<br>';

//incremento
$numero1++;
$numero2++;

echo 'suma '.$numero1.' + '.$numero2.' = '.($numero1+$numero2).'<br>';
echo 'resta '.$numero1.' + '.$numero2.' = '.($numero1-$numero2).'<br>';

//decremento
$numero1--;
$numero2--;

echo 'suma '.$numero1.' + '.$numero2.' = '.($numero1+$numero2).'<br>';
echo 'resta '.$numero1.' + '.$numero2.' = '.($numero1-$numero2).'<br>';

?>